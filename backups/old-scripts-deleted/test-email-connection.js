#!/usr/bin/env node
/**
 * Email Connection Test Script
 * 
 * Run this to diagnose email connection issues:
 * node test-email-connection.js
 */

const nodemailer = require('nodemailer');
require('dotenv').config();

console.log('═══════════════════════════════════════════════════');
console.log('📧 Email Connection Diagnostic Tool');
console.log('═══════════════════════════════════════════════════\n');

// Step 1: Check environment variables
console.log('1️⃣  Checking Environment Variables...');
const emailUser = process.env.EMAIL_USER;
const emailPassword = process.env.EMAIL_PASSWORD;

console.log(`   EMAIL_USER: ${emailUser ? '✅ ' + emailUser : '❌ NOT SET'}`);
console.log(`   EMAIL_PASSWORD: ${emailPassword ? '✅ Set (length: ' + emailPassword.length + ')' : '❌ NOT SET'}`);

if (!emailUser || !emailPassword) {
  console.log('\n❌ FAILED: Email credentials not configured!');
  console.log('\n📝 Fix:');
  console.log('   1. Create a .env file in project root');
  console.log('   2. Add these lines:');
  console.log('      EMAIL_USER=your-email@gmail.com');
  console.log('      EMAIL_PASSWORD=your-gmail-app-password');
  console.log('\n💡 Need Gmail App Password? Visit:');
  console.log('   https://myaccount.google.com/apppasswords');
  process.exit(1);
}

console.log('   ✅ Environment variables configured\n');

// Step 2: Test different SMTP configurations
async function testConnection(config, name) {
  console.log(`${config.step}  Testing ${name}...`);
  console.log(`   Host: ${config.host}`);
  console.log(`   Port: ${config.port}`);
  console.log(`   Secure: ${config.secure}`);

  const transporter = nodemailer.createTransport({
    host: config.host,
    port: config.port,
    secure: config.secure,
    auth: {
      user: emailUser,
      pass: emailPassword
    },
    connectionTimeout: 10000,
    greetingTimeout: 5000,
    socketTimeout: 15000,
    tls: {
      rejectUnauthorized: true,
      minVersion: 'TLSv1.2'
    }
  });

  try {
    console.log('   Attempting connection...');
    const startTime = Date.now();
    await transporter.verify();
    const duration = Date.now() - startTime;
    console.log(`   ✅ SUCCESS! Connected in ${duration}ms\n`);
    return true;
  } catch (error) {
    console.log(`   ❌ FAILED: ${error.message}`);
    console.log(`   Error code: ${error.code || 'N/A'}`);
    console.log(`   Command: ${error.command || 'N/A'}\n`);
    return false;
  }
}

// Step 3: Test email sending
async function sendTestEmail(config) {
  console.log('4️⃣  Sending Test Email...');
  
  const transporter = nodemailer.createTransport({
    host: config.host,
    port: config.port,
    secure: config.secure,
    auth: {
      user: emailUser,
      pass: emailPassword
    },
    connectionTimeout: 10000,
    greetingTimeout: 5000,
    socketTimeout: 15000,
    tls: {
      rejectUnauthorized: true,
      minVersion: 'TLSv1.2'
    }
  });

  try {
    const info = await transporter.sendMail({
      from: `"PixelNest Test" <${emailUser}>`,
      to: emailUser, // Send to yourself
      subject: '✅ PixelNest Email Test - SUCCESS',
      text: 'If you receive this email, your PixelNest email service is working correctly!',
      html: `
        <div style="font-family: Arial, sans-serif; padding: 20px; background: #f5f5f5;">
          <div style="background: white; padding: 30px; border-radius: 8px; max-width: 500px; margin: 0 auto;">
            <h2 style="color: #000;">✅ Email Test Successful!</h2>
            <p>Your PixelNest email service is configured correctly and working.</p>
            <div style="background: #f0f0f0; padding: 15px; border-radius: 4px; margin: 20px 0;">
              <strong>Configuration:</strong><br>
              Host: ${config.host}<br>
              Port: ${config.port}<br>
              Secure: ${config.secure}<br>
              From: ${emailUser}
            </div>
            <p style="color: #666; font-size: 12px;">
              Generated by PixelNest Email Diagnostic Tool<br>
              ${new Date().toLocaleString()}
            </p>
          </div>
        </div>
      `
    });

    console.log('   ✅ Email sent successfully!');
    console.log(`   Message ID: ${info.messageId}`);
    console.log(`   Check your inbox: ${emailUser}\n`);
    return true;
  } catch (error) {
    console.log(`   ❌ Failed to send email: ${error.message}\n`);
    return false;
  }
}

// Run diagnostic tests
async function runDiagnostics() {
  const configs = [
    {
      step: '2️⃣ ',
      name: 'Gmail SMTP (Port 587 - TLS)',
      host: 'smtp.gmail.com',
      port: 587,
      secure: false
    },
    {
      step: '3️⃣ ',
      name: 'Gmail SMTP (Port 465 - SSL)',
      host: 'smtp.gmail.com',
      port: 465,
      secure: true
    }
  ];

  let workingConfig = null;

  for (const config of configs) {
    const success = await testConnection(config, config.name);
    if (success && !workingConfig) {
      workingConfig = config;
      break; // Use the first working config
    }
  }

  console.log('═══════════════════════════════════════════════════');
  console.log('📊 Diagnostic Results');
  console.log('═══════════════════════════════════════════════════\n');

  if (workingConfig) {
    console.log('✅ Connection Test: PASSED');
    console.log(`   Working Configuration: ${workingConfig.name}\n`);

    // Try sending a test email
    const emailSent = await sendTestEmail(workingConfig);

    console.log('═══════════════════════════════════════════════════');
    console.log('🎉 Final Result');
    console.log('═══════════════════════════════════════════════════\n');

    if (emailSent) {
      console.log('✅ ALL TESTS PASSED!');
      console.log('\n✨ Your email service is working correctly!');
      console.log('\n📋 Recommended Configuration:');
      console.log(`   Host: ${workingConfig.host}`);
      console.log(`   Port: ${workingConfig.port}`);
      console.log(`   Secure: ${workingConfig.secure}`);
      console.log('\n💡 This configuration is already set in your emailService.js');
    } else {
      console.log('⚠️  Connection works but email sending failed');
      console.log('   Possible issues:');
      console.log('   - Gmail blocked the email (check security settings)');
      console.log('   - App password might be incorrect');
      console.log('   - Account might need verification');
    }
  } else {
    console.log('❌ Connection Test: FAILED');
    console.log('\n🔧 Troubleshooting Steps:\n');
    console.log('1. Verify Gmail App Password:');
    console.log('   - Go to: https://myaccount.google.com/apppasswords');
    console.log('   - Generate a new App Password');
    console.log('   - Update .env file with new password\n');
    
    console.log('2. Check Firewall/Network:');
    console.log('   Run: telnet smtp.gmail.com 587');
    console.log('   If it fails, your firewall is blocking SMTP\n');
    
    console.log('3. Try Alternative SMTP:');
    console.log('   - SendGrid: https://sendgrid.com/');
    console.log('   - Mailtrap (testing): https://mailtrap.io/\n');
    
    console.log('4. Check Server Logs:');
    console.log('   pm2 logs pixelnest-server\n');
    
    console.log('📖 Full troubleshooting guide: EMAIL_TIMEOUT_FIX.md');
  }

  console.log('\n═══════════════════════════════════════════════════\n');
}

// Run the diagnostics
runDiagnostics().catch(error => {
  console.error('❌ Diagnostic script error:', error);
  process.exit(1);
});

